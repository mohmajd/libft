int	atoi(const	char *str)
{
	int	signe;
	int	i;
	int	r;

	signe = 1;
	i = 0;
	r = 0;
	while (str[i] == ' ' || str[i] == '	')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		{
			signe *= (-1);
		}
		i++;
	}
	while (str[i] <= '9' && str[i] >= '0')
	{
		r = r * 10 + (str[i] - 48);
		i++;
	}
	r *= signe;
	return (r);
}
