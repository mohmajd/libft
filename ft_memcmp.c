#include "libft.h"

int	ft_memcmp(const void *str1, const void *str2, size_t n)
{
	char	*p1;
	char	*p2;
	size_t	i;

	p1 = (char *)str1;
	p2 = (char *)str2;
	i = 0;
	while (i < n)
	{
		if (p1[i] != p2[i])
			return (p1[i] - p2[i]);
		i++;
	}
	return (0);
}
